<h3>Битва</h3>
<p>Битва - поединок между кораблями персонажей. Игрок управляет кораблем в течение времени хода.</p>
<p>Победителем битвы считается персонаж чей корабль остается на поле битвы единственным.</p>
<p>В течение времени хода игрок указывает цели и оружие для атаки, настраивает защиту и перемещение корабля. </p>
