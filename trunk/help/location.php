<h3>Локация</h3>
<p>Небольшой объект, расположенный в секторе. Предоставляет сервисы для игроков. Бывают следующих видов:</p>
<ul>
    <li>Центр информации
    <li>Рынок
    <li>Ремонтная мастерская
    <li>Шахта
    <li>Завод
    <li>Научный центр
    <li>Фазенда
    <li>Ферма
</ul>
<p>Локация располагается вблизи или на космических объектах</p>
